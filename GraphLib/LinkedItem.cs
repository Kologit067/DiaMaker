using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GraphLib
{
    //-------------------------------------------------------------------------------------------------------
    // class LinkedItem
    //-------------------------------------------------------------------------------------------------------
    public class LinkedItem<T>
    {
        private T item;
        private LinkedItem<T> parent;
        //-------------------------------------------------------------------------------------------------------
        public LinkedItem<T> Parent
        {
            get { return parent; }
        }
        //-------------------------------------------------------------------------------------------------------
        public T Item
        {
            get { return item; }
        }
        //-------------------------------------------------------------------------------------------------------
        public LinkedItem(T pItem, LinkedItem<T> pParent)
        {
            item = pItem;
            parent = pParent;
        }
        //-------------------------------------------------------------------------------------------------------
        public override string ToString()
        {
            return Item.ToString() + (parent == null ? "" : (" -> " + parent.item.ToString()));
        }
        //-------------------------------------------------------------------------------------------------------
    }
    //-------------------------------------------------------------------------------------------------------
}
